import UIKit

extension UIButton {
    
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0, green: 0.7697899342, blue: 0.4635709524, alpha: 1)
    }
    
    func setDeselectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.6352941176, green: 0.9058823529, blue: 0.737254902, alpha: 1)
    }
    
}
